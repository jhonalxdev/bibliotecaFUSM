    public void validarLogeo (){
        
        mensage1.setText("Cuenta de correo "+ jComboBox1.getSelectedItem().toString()+" No registrada");
        
        mensage1.setVisible(false);
        mensage2.setVisible(false);
       
        
        String mailUser = jTextField1.getText();
        String passUser = jPasswordField1.getText();
        String tipoUser = jComboBox1.getSelectedItem().toString();
    
        Usuario userValid = null;

        
    if(jTextField1.getText().equals("".trim())){       // si esta en blanco el correo
        
        JOptionPane.showMessageDialog(null, "Debe completar el campo Email");
    }else if(passUser.equals("".trim())){ // si esta en blanco el password
        JOptionPane.showMessageDialog(null, "Debe completar el campo Password");        
    
    }else{    
        
        if (!validacionCorreo (mailUser, tipoUser )) {
            mensage1.setVisible(true);  
        }else{
        
            if (validacionPass(mailUser, passUser)) {
                
                userValid = buscarUsuarioxCorreo(mailUser);
 
         
                

                panelprinciapl.setUsuario(userValid);

         
                jTextField1.setText("");
                jPasswordField1.setText("");
                        
                
     
                this.setVisible(false);
                panelprinciapl.setVisible(true);
                
            } else {
                mensage2.setVisible(true);
            }
        }
    }
    }
 
    
