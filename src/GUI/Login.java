    public boolean validacionCorreo (String srtCorreo, String strTipoUser){
        
        boolean exist = false;
       
        for (Usuario u : usuariosjpa.findUsuarioEntities()) {
            
            if (u.getEmail().equals(srtCorreo)) {
                if(u.getTipoUsuario().equals(strTipoUser)){
                      exist = true;
                }
            }
        }       
      return exist;  
    }
    
    public boolean validacionPass (String srtCorreo, String srtPass){
        
        boolean valido = false;
       
        for (Usuario u : usuariosjpa.findUsuarioEntities()) {
            
            if (u.getPassword().equals(srtPass)) {
                
                if(u.getEmail().equals(srtCorreo)){                 
                        valido = true;                  
                }   
            }
        }       
      return valido;  
    }
    

    
    public Usuario buscarUsuarioxCorreo(String srtCorreo){
        Usuario user =  new Usuario();

        for (Usuario u : usuariosjpa.findUsuarioEntities()) {
             if (u.getEmail().equals(srtCorreo)) {
                user = u;
            }
        }
        return user;
    }
    
    
    
    
    
